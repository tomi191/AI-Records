// Get examples by style
export function getExamplesByStyle(style: string): TrainingExample[] {
  return TRAINING_EXAMPLES.filter(ex => ex.style === style);
}

// Get examples by mood
export function getExamplesByMood(mood: string): TrainingExample[] {
  return TRAINING_EXAMPLES.filter(ex => ex.mood === mood);
}

// Get random examples for few-shot learning
export function getRandomExamples(count: number = 2): TrainingExample[] {
  const shuffled = [...TRAINING_EXAMPLES].sort(() => 0.5 - Math.random());
  return shuffled.slice(0, count);
}

// Format example for prompt
export function formatExampleForPrompt(example: TrainingExample): string {
  return `--- Пример (${example.style}, ${example.mood}) ---
Заглавие: ${example.title}

${example.lyrics}
--- Край на примера ---`;
}
